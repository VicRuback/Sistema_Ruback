    public VarVendas viewBean() {
        VarVendas varVendas = new VarVendas();
        varVendas.setVarIdVendas(Util.strToInt(jTxtCodigo.getText()));
        varVendas.setVarClientes((VarClientes) jCboClientes.getSelectedItem() );
        varVendas.setVarUsuarios((VarUsuarios) jCboUsuarios.getSelectedItem());
        varVendas.setVarDataVendas(Util.strDate(jFmtData.getText()));

        varVendas.setVarTotal(Util.strToDouble(jTxtTotal.getText()));
        return varVendas;
    }
    
    public void beanView(VarVendas varVendas) {
        jTxtCodigo.setText(Util.intToStr(varVendas.getVarIdVendas()));
        jFmtData.setText(Util.dateToStr(varVendas.getVarDataVendas()));
        jTxtTotal.setText(Util.doubleToStr(varVendas.getVarTotal()));
        jCboClientes.setSelectedItem(varVendas.getVarClientes());
        jCboUsuarios.setSelectedItem(varVendas.getVarUsuarios());
    }


